/**
 * 领域层（Domain Layer）
 * 职责：业务建模与领域服务，是整个系统的核心
 * <p>
 * ================================================================================
 * 核心元素
 * ================================================================================
 * <p>
 * 1. 实体（Entity）
 * 1.1. 具有唯一标识的业务对象，如 User、Order
 * <p>
 * 2. 值对象（Value Object）
 * 2.1. 无标识的不可变对象，如 Address、Money
 * <p>
 * 3. 领域服务（Domain Service）
 * 3.1. 跨实体的业务逻辑
 * 3.2. 代表原子服务，不可拆分，必须同时进行的业务
 * <p>
 * 4. 仓储接口（Repository）
 * 4.1. 数据访问抽象，由基础设施层实现
 * <p>
 * 5. 领域事件（Domain Event）
 * 5.1. 领域内发生的事情，如 UserCreatedEvent
 * <p>
 * ================================================================================
 * 原子服务示例
 * ================================================================================
 * <p>
 * 用户创建 = 校验用户名唯一性 + 创建用户实体 + 初始化默认角色
 * 转账 = 校验余额 + 扣减A账户 + 增加B账户 + 记录流水
 * <p>
 * ================================================================================
 * 设计原则
 * ================================================================================
 * <p>
 * - 领域层是系统最纯净的层，不依赖任何技术框架
 * - 业务规则全部封装在领域层
 * - 仓储只定义接口，实现交给基础设施层
 *
 * @author tooolan
 * @since 2026年4月7日
 */

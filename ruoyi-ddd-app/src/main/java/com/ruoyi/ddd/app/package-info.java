/**
 * 应用层（Application Layer）
 * 职责：负责业务流程编排与复杂查询组装
 * <p>
 * ================================================================================
 * 核心功能
 * ================================================================================
 * <p>
 * 1. 查询相关
 * 1.1. 组装查询结果（多表关联、分页、排序）
 * 1.2. DTO 转换与数据聚合
 * 1.3. 调用领域服务或仓储获取数据
 * <p>
 * 2. 业务相关
 * 2.1. 编排粗粒度业务逻辑
 * 2.2. 管理事务边界（@Transactional）
 * 2.3. 协调多个领域服务完成业务流程
 * <p>
 * ================================================================================
 * 典型业务编排示例
 * ================================================================================
 * <p>
 * 用户注册 = 校验用户名 + 创建用户 + 记录日志 + 发送邮件
 * 订单创建 = 校验库存 + 创建订单 + 扣减库存 + 发布事件
 * <p>
 * ================================================================================
 * 设计原则
 * ================================================================================
 * <p>
 * - 应用服务是业务流程的组织者，不是业务规则的实现者
 * - 业务规则放在领域层，应用层只负责编排
 * - 一个应用服务方法对应一个完整的业务用例
 *
 * @author tooolan
 * @since 2026年4月7日
 */

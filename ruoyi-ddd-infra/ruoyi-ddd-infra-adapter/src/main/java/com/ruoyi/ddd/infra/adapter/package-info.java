/**
 * 框架适配器模块（Infrastructure Adapter）
 * 职责：管理零散的、不值得单独建模块的框架配置
 * <p>
 * ================================================================================
 * 核心功能
 * ================================================================================
 * <p>
 * 1. Spring 扩展
 * 1.1. Bean 后置处理器、自定义注解处理
 * <p>
 * 2. 线程池配置
 * 2.1. 异步任务线程池、自定义线程池
 * <p>
 * 3. 日志配置
 * 3.1. 日志格式、日志级别、TraceId 处理
 * <p>
 * 4. 序列化配置
 * 4.1. Jackson 自定义序列化器
 * <p>
 * 5. 工具类集成
 * 5.1. Hutool、TTL 等工具的初始化
 * <p>
 * ================================================================================
 * 模块定位
 * ================================================================================
 * <p>
 * 这是一个"杂项配置"模块，收集那些：
 * - 配置内容较少，不值得新建模块
 * - 与具体业务无关，属于框架层面的定制
 * - 需要覆盖框架默认行为的配置
 * <p>
 * ================================================================================
 * 与其他模块的区别
 * ================================================================================
 * <p>
 * vs infra-db
 * - 数据库配置复杂且独立，需要专门模块管理
 * - 日志、线程池等配置简单，放这里即可
 * <p>
 * ================================================================================
 * 设计原则
 * ================================================================================
 * <p>
 * - 约定大于配置的个性化定制
 * - 避免配置膨胀，复杂配置应拆分独立模块
 *
 * @author tooolan
 * @since 2026年4月7日
 */
package com.ruoyi.ddd.infra.adapter;
